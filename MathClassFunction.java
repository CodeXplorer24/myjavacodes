package BasicsOfJava;

public class MathClassFunction {
    public static void main (String[] args){
        System.out.println(Math.PI);
        System.out.println(Math.E);
        System.out.println(Math.pow(3,5));
        System.out.println(Math.sqrt(3600));
        System.out.println(Math.max(50,400));

        int num = -887;
        System.out.println("Absolute value = " + Math.abs(num));

        double fraction = 5.667;
        System.out.println("Absolute value = " + Math.abs(fraction));


    }
}
